def get_all_ssr_link():
    # 拼接SSR服务器地址
    ssr_lists = [("http://ss.pythonic.life" + i) for i in a]
    list = []
    for i in ssr_lists:
        xpath_rule = '//*[@id="uri"]/input/@value'
        he = get_source_html(i, xpath_rule)
