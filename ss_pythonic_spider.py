# 用作存储请求的html源码
a = []
source_link = "http://ss.pythonic.life/"

# 请求网页源码,并使用xpath规则提取
def get_source_html(url, xpath_rule):
    """
        # 请求链接,然后etree格式化,之后用xpath规则把所需数据提取
    """
    r = requests.get(url, headers={"User-Agent": random.choice(user_agent)})
    h = etree.HTML(r.text)
    h_data = h.xpath(xpath_rule)
    return h_data


# 主函数,先请求源地址
def main():
    """
        # 请求源地址
    """
    print(a)
    xpath_rule = "//fieldset/ol/li/a/@href"
    h_data = get_source_html(source_link, xpath_rule)
    for i in h_data:
        a.append(i)
    if not a:
        time.sleep(1)
        main()
    else:
        pass


